# 6. Ввести с клавиатуры два целых числа: x и y
x = 9
y = 4
#
#    - Вывести на экран консоли оба числа используя только один вызов print()
print(x,';', y)
#
#    - Вычислить сумму чисел
print(x + y)
#    - Выполнить целочисленное деление
print(x // y)
#    - Найти остаток
print(x - (y * (x // y)))
#    - Вычислить степень числа: x^y
print(x ** y)
